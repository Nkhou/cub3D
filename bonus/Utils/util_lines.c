#include "../../include/cub.h"

void	check_pos_line_c(char *str, int i, t_tex *tex)
{
	if (str[i] == 'C' && str[i + 1] == ' ')
	{
		while (str[i] != '\n')
			i++;
		tex->c++;
	}
}
